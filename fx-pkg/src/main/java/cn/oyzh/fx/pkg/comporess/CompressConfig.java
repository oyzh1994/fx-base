package cn.oyzh.fx.pkg.comporess;


/**
 * 压缩配置
 *
 * @author oyzh
 * @since 2024/06/18
 */
public class CompressConfig {

    /**
     * 压缩类型
     */
    private String type;

    /**
     * 压缩文件名
     */
    private String name;

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
